package io.nop.commons.functional;

import java.util.Objects;

@FunctionalInterface
public interface ITriPredicate<T, U, V> {
    boolean test(T t, U u, V v);

    default ITriPredicate<T, U, V> and(ITriPredicate<? super T, ? super U, ? super V> other) {
        Objects.requireNonNull(other);
        return (T t, U u, V v) -> test(t, u, v) && other.test(t, u, v);
    }

    default ITriPredicate<T, U, V> or(ITriPredicate<? super T, ? super U, ? super V> other) {
        Objects.requireNonNull(other);
        return (T t, U u, V v) -> test(t, u, v) || other.test(t, u, v);
    }
}