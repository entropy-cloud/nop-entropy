CREATE TABLE nop_job_task(
  JOB_TASK_ID VARCHAR(32) NOT NULL    COMMENT '任务ID',
  JOB_FIRE_ID VARCHAR(32) NOT NULL    COMMENT '批次ID',
  TASK_NO INTEGER default 1  NOT NULL    COMMENT '任务序号',
  TASK_STATUS INTEGER NOT NULL    COMMENT '任务状态',
  WORKER_INSTANCE_ID VARCHAR(100) NULL    COMMENT '执行节点ID',
  WORKER_ADDRESS VARCHAR(200) NULL    COMMENT '执行节点地址',
  TASK_PAYLOAD VARCHAR(4000) NULL    COMMENT '投递参数',
  START_TIME DATETIME(3) NULL    COMMENT '开始时间',
  END_TIME DATETIME(3) NULL    COMMENT '结束时间',
  DURATION_MS BIGINT NULL    COMMENT '执行时长(毫秒)',
  RESULT_PAYLOAD VARCHAR(4000) NULL    COMMENT '执行结果',
  ERROR_CODE VARCHAR(200) NULL    COMMENT '错误码',
  ERROR_MESSAGE VARCHAR(1000) NULL    COMMENT '错误消息',
  PARTITION_INDEX SMALLINT NOT NULL    COMMENT '分区索引',
  VERSION BIGINT NOT NULL    COMMENT '数据版本',
  CREATED_BY VARCHAR(50) NOT NULL    COMMENT '创建人',
  CREATE_TIME DATETIME(3) DEFAULT CURRENT_TIMESTAMP(3)  NOT NULL    COMMENT '创建时间',
  UPDATED_BY VARCHAR(50) NOT NULL    COMMENT '修改人',
  UPDATE_TIME DATETIME(3) DEFAULT CURRENT_TIMESTAMP(3)  NOT NULL    COMMENT '修改时间',
  REMARK VARCHAR(200) NULL    COMMENT '备注',
  PROGRESS INTEGER NULL    COMMENT '执行进度',
  PROGRESS_MESSAGE VARCHAR(500) NULL    COMMENT '进度消息',
  TARGET_HOST VARCHAR(200) NULL    COMMENT '目标节点地址',
  SHARDING_INDEX INTEGER NULL    COMMENT '分片索引',
  SHARDING_TOTAL INTEGER NULL    COMMENT '总分片数',
  COST_CPU INTEGER default 0  NULL    COMMENT 'CPU开销(毫核)',
  COST_MEMORY INTEGER default 0  NULL    COMMENT '内存开销(MB)',
  constraint PK_nop_job_task primary key (JOB_TASK_ID)
)CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_as_cs;


