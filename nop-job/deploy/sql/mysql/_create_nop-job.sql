
CREATE TABLE nop_job_schedule(
  JOB_SCHEDULE_ID VARCHAR(32) NOT NULL    COMMENT '调度ID',
  NAMESPACE_ID VARCHAR(50) NULL    COMMENT '命名空间',
  GROUP_ID VARCHAR(100) NULL    COMMENT '分组',
  JOB_NAME VARCHAR(100) NOT NULL    COMMENT '作业名',
  DISPLAY_NAME VARCHAR(200) NOT NULL    COMMENT '显示名',
  DESCRIPTION VARCHAR(4000) NULL    COMMENT '描述',
  SCHEDULE_STATUS INTEGER NOT NULL    COMMENT '调度状态',
  EXECUTOR_KIND VARCHAR(50) NULL    COMMENT '执行器类型',
  JOB_PARAMS VARCHAR(4000) NULL    COMMENT '任务参数',
  TRIGGER_TYPE INTEGER NULL    COMMENT '触发器类型',
  CRON_EXPR VARCHAR(100) NULL    COMMENT 'CRON表达式',
  REPEAT_INTERVAL_MS BIGINT NULL    COMMENT '重复间隔(毫秒)',
  MAX_EXECUTION_COUNT INTEGER NULL    COMMENT '最大执行次数',
  MIN_SCHEDULE_TIME DATETIME(3) NULL    COMMENT '最早调度时间',
  MAX_SCHEDULE_TIME DATETIME(3) NULL    COMMENT '最晚调度时间',
  MISFIRE_THRESHOLD_MS INTEGER NULL    COMMENT 'Misfire阈值(毫秒)',
  USE_DEFAULT_CALENDAR TINYINT default 0  NULL    COMMENT '使用默认日历',
  PAUSE_CALENDAR_SPEC VARCHAR(4000) NULL    COMMENT '暂停日历配置',
  BLOCK_STRATEGY INTEGER NULL    COMMENT '阻塞策略',
  TIMEOUT_SECONDS INTEGER NULL    COMMENT '超时时间(秒)',
  RETRY_POLICY_ID VARCHAR(32) NULL    COMMENT '重试策略ID',
  PARTITION_INDEX SMALLINT NOT NULL    COMMENT '分区索引',
  FIRE_COUNT BIGINT default 0  NOT NULL    COMMENT '已触发次数',
  ACTIVE_FIRE_COUNT INTEGER default 0  NOT NULL    COMMENT '活跃触发数',
  LAST_FIRE_TIME DATETIME(3) NULL    COMMENT '上次触发时间',
  LAST_END_TIME DATETIME(3) NULL    COMMENT '上次结束时间',
  NEXT_FIRE_TIME DATETIME(3) NULL    COMMENT '下次触发时间',
  LAST_FIRE_STATUS INTEGER NULL    COMMENT '上次触发状态',
  VERSION BIGINT NOT NULL    COMMENT '数据版本',
  CREATED_BY VARCHAR(50) NOT NULL    COMMENT '创建人',
  CREATE_TIME DATETIME(3) DEFAULT CURRENT_TIMESTAMP(3)  NOT NULL    COMMENT '创建时间',
  UPDATED_BY VARCHAR(50) NOT NULL    COMMENT '修改人',
  UPDATE_TIME DATETIME(3) DEFAULT CURRENT_TIMESTAMP(3)  NOT NULL    COMMENT '修改时间',
  REMARK VARCHAR(200) NULL    COMMENT '备注',
  LAST_DURATION_MS BIGINT NULL    COMMENT '上次执行耗时(毫秒)',
  TOTAL_FIRE_COUNT BIGINT default 0  NULL    COMMENT '总触发次数',
  SUCCESS_FIRE_COUNT BIGINT default 0  NULL    COMMENT '成功触发次数',
  FAIL_FIRE_COUNT BIGINT default 0  NULL    COMMENT '失败触发次数',
  TASK_COST_CPU INTEGER default 0  NULL    COMMENT '任务CPU开销(毫核)',
  TASK_COST_MEMORY INTEGER default 0  NULL    COMMENT '任务内存开销(MB)',
  constraint PK_nop_job_schedule primary key (JOB_SCHEDULE_ID)
)CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_as_cs;

CREATE TABLE nop_job_fire(
